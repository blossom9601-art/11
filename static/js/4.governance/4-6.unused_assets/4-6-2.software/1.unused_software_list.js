    function buildBulkForm(){
        const form = document.getElementById(BULK_FORM_ID); if(!form) return;
        const EXCLUDE = new Set([]);
        function inputFor(col){
            if(col === 'work_status'){
                return `<select class="form-input" data-bulk-field="work_status">
                    <option value="">선택</option>
                    <option value="가동">가동</option>
                    <option value="유휴">유휴</option>
                    <option value="대기">대기</option>
                </select>`;
            }
            if(col === 'work_group'){
                return `<input class="form-input" data-bulk-field="work_group" placeholder="업무 그룹">`;
            }
            if(col === 'sw_class'){
                return `<select class="form-input" data-bulk-field="sw_class">
                    <option value="">선택</option>
                    <option value="상용">상용</option>
                    <option value="오픈소스">오픈소스</option>
                </select>`;
            }
            if(col === 'erp_no') return `<input class="form-input" data-bulk-field="erp_no" placeholder="ERP번호">`;
            if(col === 'acq_date') return `<input class="form-input" data-bulk-field="acq_date" placeholder="YYYY-MM-DD">`;
            if(col === 'acq_cost') return `<input type="number" min="0" step="1" class="form-input" data-bulk-field="acq_cost" placeholder="취득비용">`;
            if(col === 'residual_value') return `<input type="number" min="0" step="1" class="form-input" data-bulk-field="residual_value" placeholder="잔존가치">`;
            if(col === 'remark') return `<textarea class="form-input textarea-large" rows="6" data-bulk-field="remark" placeholder="비고"></textarea>`;
            return `<input class="form-input" data-bulk-field="${col}" placeholder="값 입력">`;
        }
        const GROUPS = [
            { title:'비즈니스', cols:['work_status','work_group'] },
            { title:'소프트웨어', cols:['sw_type','sw_class','sw_vendor','sw_name','sw_version'] },
            { title:'담당자', cols:['sw_dept','sw_owner','service_dept','service_owner'] },
            // 불용처리: 라이선스 필드 제거, 자산 편입 관련 필드만 제공
            { title:'불용처리', cols:['erp_no','acq_date','acq_cost','residual_value','remark'] }
        ];
        form.innerHTML = GROUPS.map(g=>{
            const fields = g.cols.filter(c=> !EXCLUDE.has(c));
            if(!fields.length) return '';
            const grid = fields.map(col=>{
                const meta = COLUMN_META[col]; if(!meta) return '';
                const wide = (col === 'remark');
                const nowrap = '';
                const text = meta.label;
                return `<div class="${wide ? 'form-row form-row-wide' : 'form-row'}"><label${nowrap}>${text}</label>${inputFor(col)}</div>`;
            }).join('');
            return `
                <div class="form-section">
                    <div class="section-header"><h4>${g.title}</h4></div>
                    <div class="form-grid">${grid}</div>
                </div>`;
        }).join('');
        // 수정 모달과 동일하게 날짜/계산 동기화 적용
        // no license live sync for disposal fields
        initDatePickers(BULK_FORM_ID);
    }

