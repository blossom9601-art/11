    function buildStats(){
    const swEl = document.getElementById('stats-software');
    const verEl = document.getElementById('stats-versions');
    const checkEl = document.getElementById('stats-check');
        if(swEl) swEl.innerHTML = '';
        if(verEl) verEl.innerHTML = '';
        if(checkEl) checkEl.innerHTML = '';
        // 대상 데이터: 현재 필터/정렬 적용 전부를 기준으로 통계 (state.filtered)
        const rows = state.filtered.length ? state.filtered : state.data;
        // 센터 통계
        // Bin seismic strengths by nearest 0.5 for a compact histogram
        const seismicDist = rows.reduce((acc, r)=>{
            const v = r.seismic;
            if(v === '' || v == null) return acc;
            const num = parseFloat(v);
            if(!Number.isFinite(num)) return acc;
            const binned = Math.round(num * 2) / 2; // nearest 0.5
            const key = binned.toFixed(1);
            acc[key] = (acc[key]||0)+1;
            return acc;
        }, {});
        renderStatBlock('stats-software', '내진설계(강도)', seismicDist);
        renderStatBlock('stats-versions', '용도', countBy(rows, 'usage'));
        renderStatBlock('stats-check', '지역', countBy(rows, 'location'));
    }

