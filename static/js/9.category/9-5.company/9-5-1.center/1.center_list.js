    async function updateRow(rowId, data){
        if(!rowId) return null;
        const payload = await apiRequest(`${API_ENDPOINT}/${rowId}`, {
            method: 'PUT',
            body: JSON.stringify(data || {}),
        });
        const item = normalizeRow(payload?.item);
        if(item){
            state.data = state.data.map(row => row.id === rowId ? item : row);
            applyFilter();
        }
        return item;
    }

    async function deleteRows(rowIds){
        if(!Array.isArray(rowIds) || rowIds.length === 0) return 0;
        const ids = rowIds.filter(id => typeof id === 'number' && !Number.isNaN(id));
        if(!ids.length) return 0;
        await apiRequest(`${API_ENDPOINT}/bulk-delete`, {
            method: 'POST',
            body: JSON.stringify({ ids }),
        });
        const idSet = new Set(ids);
        state.data = state.data.filter(row => !idSet.has(row.id));
        state.selected.clear();
        applyFilter();
        return ids.length;
    }

